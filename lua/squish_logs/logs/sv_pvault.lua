hook.Add("pVaultMoneyCleaned", "SquishLogs:Log:pVault:CleanMoney", function(ply, amount)
    SquishLog:New()
        :SetCategory("pVault | Money")
        :AddFragment(ply)
        :AddFragment(SquishLogs:GetPhrase("money_clean"))
        :SetFragmentColor(colorGreen)
        :AddFragment("$"..string.Comma(amount))
        :Send()
end)

hook.Add("pVaultVaultCracked", "SquishLogs:Log:pVault:CrackVault", function(vault, ply)
    SquishLog:New()
        :SetCategory("pVault | Vault")
        :AddFragment(ply)
        :AddFragment(SquishLogs:GetPhrase("crack_vault"))
        :Send()
end)

