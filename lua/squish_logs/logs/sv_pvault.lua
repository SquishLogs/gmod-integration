local colorGreen = Color(34,139,34)

hook.Add("pVaultMoneyCleaned", "SquishLogs:Log:pVault:CleanMoney", function(ply, amount)
    SquishLog:New()
        :SetCategory("pVault | Money")
        :AddFragment(ply)
        :AddFragment(' cleaned money for the amount of ')
        :SetFragmentColor(colorGreen)
        :AddFragment("$"..string.Comma(amount))
        :Send()
end)

hook.Add("pVaultVaultCracked", "SquishLogs:Log:pVault:CrackVault", function(vault, ply)
    SquishLog:New()
        :SetCategory("pVault | Vault")
        :AddFragment(ply)
        :AddFragment(' cracked a vault')
        :Send()
end)

hook.Add("pVaultVaultCrackFailed", "SquishLogs:Log:pVault:CrackVault", function(vault, ply)
    SquishLog:New()
        :SetCategory("pVault | Vault")
        :AddFragment(ply)
        :AddFragment(' attempted to crack a vault but failed')
        :Send()
end)