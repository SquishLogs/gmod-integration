local colorGreen = Color(34,139,34)

hook.Add("pCasinoOnBlackjackBet", "SquishLogs:Log:pCasino:Blackjack", function(ply, machine, amount)
    SquishLog:New()
        :SetCategory("pCasino | Blackjack")
        :AddFragment(ply)
        :AddFragment(SquishLogs:GetPhrase("bet_placed"))
        :SetFragmentColor(colorGreen)
        :AddFragment("$"..string.Comma(amount))
        :Send()
end)

